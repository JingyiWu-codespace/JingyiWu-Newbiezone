package Java30days_韩顺平.Chapter1to6;

public class HomeworkControlFlow04 {
    public static void main(String[] args){
        for (char c1='a'; c1<='z';c1++ ){
            System.out.print(c1);
        }
        System.out.println(" ");
        System.out.println("========");
        for (char d1='Z'; d1>='A';d1-- ){
            System.out.print(d1);
        }
    }
}
